package tij.c3;

public class TestUnaryOperators {

    public static void main(String[] args) {

        int a = 2;
        a += 2;
        System.out.println(a);

        a -= 2;
        System.out.println(a);

        a *= 2;
        System.out.println(a);

        a /= 2;
        System.out.println(a);

        a %= 2;
        System.out.println(a);

    }

}
