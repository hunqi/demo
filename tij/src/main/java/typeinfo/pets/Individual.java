package typeinfo.pets;

public class Individual {

    private String name;

    public Individual() {
    }

    public Individual(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }
}
